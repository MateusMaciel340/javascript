// Tipos funções

// Declarativas
function Funcao(){
    console.log("Essa é uma função sendo executada");
}

Funcao();

// Expressões de funções
var funcao = function /*funcao*/ (){
    console.log("Sou uma função sendo executada com uma expressão!");
}

funcao();

// Arrow Function 
var funcao = () => {
    console.log("Sou um array function");
}

funcao();