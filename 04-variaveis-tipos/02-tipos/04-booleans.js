// Booleans -> True ou false

let validation = 3 === 0;
console.log(validation);

validation = 3 === 3;
console.log(validation);

validation = 3 > 4;
console.log(validation);

validation = 3 >= 2;
console.log(validation);

console.log(validation.toString());

// Negatividade! True para false, ou false para true.
console.log(!validation);