// Numbers - números inteiros ou decimais

// Declaração
let numero = 101;
console.log(numero);

// Operações matemáticas
console.log(
    numero + 2, numero - 2, numero * 2, numero / 2, numero % 2);

// Math
console.log(typeof Math);
console.log(Math.PI);

// Conversão
let fiveByThree = 5 / 3;
console.log(fiveByThree);
console.log(Math.floor(fiveByThree));
console.log(Math.ceil(fiveByThree));

// Porcentagem - Agrupamento de number com string, sem alterar tipos!
let percent = 10;
console.log(percent + "%");
console.log(typeof percent);