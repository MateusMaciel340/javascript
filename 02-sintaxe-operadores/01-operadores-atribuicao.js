// Operadores de atribuição

let numero_1 = 6;
let numero_2 = 8;


// 01 - Operador -> = Atribuindo um novo valor!
numero_1 = 7;
numero_2 = 9;

console.log(numero_1, numero_2);

// 02 - Operador += Ele vai somar os dois valores
console.log(numero_1 += numero_2);

// 03 - Operador *= Ele vai multiplicar os dois valores
console.log(numero_1 *= numero_2);

// 04 - Operador /= Ele vai dividir os dois valores
console.log(numero_1 /= numero_2);

// 05 - Operador %= O resto de seria ...
console.log(numero_1 %= numero_2);