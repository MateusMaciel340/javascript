let numero_1_igual = 8;
let numero_2_igual = 8;


// Operador == (Igual?)
if(numero_1_igual == numero_2_igual){
    console.log(`${numero_1_igual} é igual ${numero_2_igual}`);
}else{
    console.log(`${numero_1_igual} não é igual a ${numero_2_igual}`);
}

// Operador === (Mesmo tipo e mesmo valor)
if(numero_1_igual === numero_2_igual){
    console.log(`${numero_1_igual} tem o mesmo valor e tipo que o ${numero_2_igual}`);
}else{
    console.log(`${numero_1_igual} não tem o mesmo valor e tipo que o ${numero_2_igual}`);
}

// Operador != (Diferente)
if(numero_1_igual !== numero_2_igual){
    console.log(`${numero_1_igual} é diferente do ${numero_2_igual}`);
}else{
    console.log(`${numero_1_igual} é igual ao ${numero_2_igual}`);
}

// Operador > (Maior)
if(numero_1_igual > numero_2_igual){
    console.log(`${numero_1_igual} é maior que ${numero_2_igual}`);
}else{
    console.log(`${numero_1_igual} não é maior que ${numero_2_igual}`);
}

// Operador < (Menor)
if(numero_1_igual < numero_2_igual){
    console.log(`${numero_1_igual} é menor que o número ${numero_2_igual}`);
}else{
    console.log(`${numero_1_igual} não é menor que ${numero_2_igual}`);
}

// Operador >= (Maior ou igual)
if(numero_1_igual >= numero_2_igual){
    console.log(`${numero_1_igual} é maior ou igual ao ${numero_2_igual}`);
}else{
    console.log(`${numero_1_igual} não é maior ou igual ao ${numero_2_igual}`);
}

// Operador <= (Menor ou igual)
if(numero_1_igual <= numero_2_igual){
    console.log(`${numero_1_igual} é menor ou igual ao ${numero_2_igual}`);
}else{
    console.log(`${numero_1_igual} não é menor ou igual ao ${numero_2_igual}`);
}