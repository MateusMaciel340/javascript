let numero_1 = 6;
let numero_2 = 9;

// Operador +
console.log(numero_1 + numero_2);

// Operador -
console.log(numero_1 - numero_2);

// Operador *
console.log(numero_1 * numero_2);

// Operador **
console.log(numero_1 ** numero_2);

// Operador /
console.log(numero_1 / numero_2);

// Operador %
console.log(numero_1 % numero_2);

// Operador ++
console.log(numero_1 ++);

// Operador --
console.log(numero_2 --);